def get_message(key: str, language: str = 'ru', **kwargs) -> str:
    """Get localized message with optional formatting"""
    try:
        if key not in MESSAGES:
            logging.error(f"Message key '{key}' not found in MESSAGES")
            return f"Message key '{key}' not found"
        
        message = MESSAGES[key].get(language, MESSAGES[key].get('ru', ''))
        
        if kwargs:
            try:
                return message.format(**kwargs)
            except KeyError as e:
                logging.error(f"Missing format parameter: {e}")
                return message
        
        logging.debug(f"get_message: key='{key}', language='{language}', result='{message[:50]}...'")
        return message
        
    except Exception as e:
        logging.error(f"Error in get_message: {e}")
        return f"Error loading message: {key}"
